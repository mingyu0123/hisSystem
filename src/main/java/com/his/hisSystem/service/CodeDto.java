package com.his.hisSystem.service;

public class CodeDto {

	private int codeSeq;
	private String code;
	private String codeName;
	private String codeType;
	private String codeUpper;
	private int codeOrder;
	private String delYn;
	private String insDate;
	private String uptDate;
	public int getCodeSeq() {
		return codeSeq;
	}
	public void setCodeSeq(int codeSeq) {
		this.codeSeq = codeSeq;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getCodeName() {
		return codeName;
	}
	public void setCodeName(String codeName) {
		this.codeName = codeName;
	}
	public String getCodeType() {
		return codeType;
	}
	public void setCodeType(String codeType) {
		this.codeType = codeType;
	}
	public String getCodeUpper() {
		return codeUpper;
	}
	public void setCodeUpper(String codeUpper) {
		this.codeUpper = codeUpper;
	}
	public int getCodeOrder() {
		return codeOrder;
	}
	public void setCodeOrder(int codeOrder) {
		this.codeOrder = codeOrder;
	}
	public String getDelYn() {
		return delYn;
	}
	public void setDelYn(String delYn) {
		this.delYn = delYn;
	}
	public String getInsDate() {
		return insDate;
	}
	public void setInsDate(String insDate) {
		this.insDate = insDate;
	}
	public String getUptDate() {
		return uptDate;
	}
	public void setUptDate(String uptDate) {
		this.uptDate = uptDate;
	}
	
	
}
